package BTVN_B5;

public class If_else {
    public static void main(String[] args) {
        int number = 100;
        if (number == 100) {
            System.out.println("Number là: " + number);
        } else if (number < 50) {
            System.out.println("Number nhỏ hơn 50");
        } else if (number > 50) {
            System.out.println("Numer lớn hơn 50");
        }
    }
}
